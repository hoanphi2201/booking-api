class BaseCamelKey:
    mapping = {}

    @classmethod
    def reverse_to_snake_key(cls, **camel_key_data):
        result = {}
        for key, value in cls.mapping.items():
            if value in camel_key_data:
                result[key] = camel_key_data[value]

        return result


