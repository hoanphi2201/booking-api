class GlobalCamelKey(BaseCamelKey):
    mapping = {}

    for cls in [HotelCamelKey, TerminalCamelKey, DeliveryMethodsCamelKey, SellersTerminalsCamelKey]:
        for key, value in cls.mapping.items():
            if not mapping.get(key):
                mapping[key] = value
